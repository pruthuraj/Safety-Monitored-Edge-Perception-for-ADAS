"""Convert KITTI labels to YOLO format and build the training layout.

For each split (train/val/test, from configs/splits/):
  - hardlink images into data/processed/kitti_yolo/images/<split>/  (no disk copy)
  - write YOLO labels into data/processed/kitti_yolo/labels/<split>/
  - classes per src/dataset/kitti_classes.py; excluded types dropped by policy

YOLO label line: <class> <cx> <cy> <w> <h>, all normalized to [0,1] by true
per-image dimensions (KITTI image sizes vary slightly).

Also writes configs/kitti_yolov8.yaml pointing at the processed layout.

Usage:
    python -m src.dataset.kitti_to_yolo --root data/raw/kitti
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

from PIL import Image

from src.dataset.kitti_classes import CLASS_MAP, CLASS_NAMES, EXCLUDED_TYPES

REPO = Path(__file__).resolve().parents[2]


def convert_label(label_path: Path, img_w: int, img_h: int) -> tuple[list[str], dict]:
    """Return YOLO label lines + per-file stats. Raises on unmapped types."""
    lines: list[str] = []
    stats = {"kept": 0, "excluded": 0}
    for raw in label_path.read_text().splitlines():
        if not raw.strip():
            continue
        f = raw.split()
        obj_type = f[0]
        if obj_type in EXCLUDED_TYPES:
            stats["excluded"] += 1
            continue
        if obj_type not in CLASS_MAP:
            raise ValueError(f"{label_path.name}: unmapped KITTI type {obj_type!r}")
        cls = CLASS_MAP[obj_type]
        left, top, right, bottom = (float(v) for v in f[4:8])
        # clamp to image bounds, then normalize
        left, right = max(0.0, left), min(float(img_w), right)
        top, bottom = max(0.0, top), min(float(img_h), bottom)
        w, h = right - left, bottom - top
        if w <= 1.0 or h <= 1.0:  # degenerate box after clamping
            stats["excluded"] += 1
            continue
        cx, cy = (left + right) / 2.0 / img_w, (top + bottom) / 2.0 / img_h
        nw, nh = w / img_w, h / img_h
        assert 0.0 <= cx <= 1.0 and 0.0 <= cy <= 1.0 and 0.0 < nw <= 1.0 and 0.0 < nh <= 1.0
        lines.append(f"{cls} {cx:.6f} {cy:.6f} {nw:.6f} {nh:.6f}")
        stats["kept"] += 1
    return lines, stats


def build(root: Path, splits_dir: Path, out_root: Path, yaml_path: Path) -> dict:
    image_dir = root / "training" / "image_2"
    label_dir = root / "training" / "label_2"
    summary: dict = {"splits": {}, "class_names": CLASS_NAMES}

    for split_file in ["train.txt", "val.txt", "test.txt"]:
        split = split_file.removesuffix(".txt")
        ids = (splits_dir / split_file).read_text().split()
        img_out = out_root / "images" / split
        lbl_out = out_root / "labels" / split
        img_out.mkdir(parents=True, exist_ok=True)
        lbl_out.mkdir(parents=True, exist_ok=True)

        class_counts = [0] * len(CLASS_NAMES)
        n_excluded = 0
        for sid in ids:
            src_img = image_dir / f"{sid}.png"
            dst_img = img_out / f"{sid}.png"
            if not dst_img.exists():
                os.link(src_img, dst_img)  # hardlink: zero-copy, same volume
            with Image.open(src_img) as im:
                img_w, img_h = im.size
            lines, stats = convert_label(label_dir / f"{sid}.txt", img_w, img_h)
            (lbl_out / f"{sid}.txt").write_text("\n".join(lines) + ("\n" if lines else ""))
            n_excluded += stats["excluded"]
            for ln in lines:
                class_counts[int(ln.split()[0])] += 1

        summary["splits"][split] = {
            "images": len(ids),
            "boxes_per_class": dict(zip(CLASS_NAMES, class_counts)),
            "excluded_boxes": n_excluded,
        }
        for cname, cnt in zip(CLASS_NAMES, class_counts):
            if cnt == 0:
                print(f"WARNING: split {split!r} has zero {cname!r} boxes", file=sys.stderr)

    yaml_path.write_text(
        "# Generated by src/dataset/kitti_to_yolo.py — do not edit by hand.\n"
        f"path: {out_root.resolve().as_posix()}\n"
        "train: images/train\n"
        "val: images/val\n"
        "test: images/test\n"
        "names:\n" + "".join(f"  {i}: {n}\n" for i, n in enumerate(CLASS_NAMES))
    )
    summary["yaml"] = str(yaml_path)
    return summary


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--root", type=Path, default=Path("data/raw/kitti"))
    ap.add_argument("--splits", type=Path, default=Path("configs/splits"))
    ap.add_argument("--out", type=Path, default=Path("data/processed/kitti_yolo"))
    ap.add_argument("--yaml", type=Path, default=Path("configs/kitti_yolov8.yaml"))
    args = ap.parse_args()

    summary = build(args.root, args.splits, args.out, args.yaml)
    print(json.dumps(summary, indent=2))
    Path("results").mkdir(exist_ok=True)
    Path("results/conversion_summary.json").write_text(json.dumps(summary, indent=2))
    return 0


if __name__ == "__main__":
    sys.exit(main())
